'''
Vytvorte string s vašim menom a priezviskom.
Vypíšte ho veľkými písmenami, malými písmenami a s prvým veľkým písmenom.
'''

# Vaše riešenie:

meno = input("Zadaj mi svoje meno")

# vypis v roznych formach
print("Pôvodný tvar:", meno)
print("Veľkými písmenami:", meno.upper())
print("Malými písmenami:", meno.lower())
print("S prvým veľkým písmenom:", meno.title())

